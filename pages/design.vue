      <p>
        <b
          >You build design your deisgn skills the same way you build muscle.</b
        >
        Without putting in the work it is impossible to get better.
      </p>
      <p>
        There are no shortcuts&mdash;Put in the work consistently and you will
        get better. The designers you look up to have tens of thousands of hours
        of time they have put into their craft.
      </p>
      <p>
        Try recreating apps or websites you like in your design tool. It's ok to
        copy them pixel for pixel, as long as you don't try to claim them as
        your own work. This type of exercise will help you learn how designers
        lay out screens, and help you become faster when working on your own
        projects.
      </p>
    </li>
    <li>
      <p>
        <b>Don't treat your work as precious.</b> Get feedback early and often,
        and be willing to change your approach and explore alternatives.
      </p>
      <p>
        Critical (constructive) feedback is the single most important thing you
        can receive from others to develop your skills. If you feel hesitant
        about asking for feeback about something, that is almost always a sign
        that you should get feedback.
      </p>
      <p>
        You will get better feedback sharing your work throught it's
        development. Here are three useful times to get feedback:
      </p>
      <ol>
        <li>When you first develop an idea</li>
        <li>
          After you have explored at least 3-4 directions (never share only a
          single direction at this stage!)
        </li>
        <li>Following a deep dive on a promising direction</li>
      </ol>
      <p>
        If you get feedback at these three points you will most often recieve
        actionable feedback, rather than receving feedback on new directions
        after a project is close to being finished.
      </p>
      <p>
        The best designers I know get feedback early and often from their peers,
        both on a 1:1 basis, and when available, in a formal critique setting.
      </p>
    </li>
    <li>Style isn't important to be a good designer.</li>
    <li>
      <p>
        Don't start from design systems when creating your own work. Design
        systems are a way to organize large projects with multiple
        collaborators, and build consistency across many screens.
      </p>
      <p>
        It can be harmful to constrain your exploration early in a project by
        trying to create systems. Design systems are very important, but may not
        build the muscles early career designers need to develop a solid
        foundation.
      </p>
    </li>
  </ul>
