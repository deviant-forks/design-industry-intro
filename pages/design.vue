  <h2>Have a portfolio</h2>
  <p>
    Your portfolio doesn't need to be unique or ornate. A portfolio is simply a
    vehicle to share your work. The number one mistake early career designers
    make is spending far too much time creating a portfolio with hardly any work
    in it.
  </p>
  <p>
    Try spending no more than a week on your first portfolio, and check in on
    how you are investing your time by roughly using a 1:4 ratio of time spent
    when building a portfolio to working on projects.
