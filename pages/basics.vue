    <h2>Choosing your toolkit</h2>
    <p>
      It is easy to get overwhelmed with the number of tools and the mixed
      messages coming from all sides on which tools to learn.
    </p>
    <p>
      Remember, the goal of a tool is to help you create better work – when
      learning a tool becomes a distraction from making work and solving
      problems, consider taking a step back and thinking through if you need a
      new tool.
    </p>
    <p><b>A core tool kit for a designer starting to build products is:</b></p>
    <ul>
      <li>One design tool (Figma, Sketch, XD)</li>
      <li>One prototyping tool (Protopie, Figma, Origami Studio, Framer)</li>
      <li>One presentation tool (Keynote, Google Slides, Figma)</li>
    </ul>
    <p><b>Additionally, the following may be helpful:</b></p>
    <ul>
      <li>One notes & sheets tool (Notion, Google Drive, an Office suite)</li>
      <li>One image tool (Photoshop, Affinity Designer/Photo)</li>
      <li>One motion tool (After Effects, Final Cut, Premiere)</li>
    </ul>
    <p>
      It may be cost-effective to initially align yourself with a family of apps
      in the beginning. For example, the
      <a href="https://affinity.serif.com/en-us/">Affinity</a> tools (Designer,
      Photo, Publisher) have only an upfront cost, not a continuous monthly
      subscription.
    </p>
