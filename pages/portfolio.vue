          >Read this</a
        >, then write up the projects you have, up to three.
      </li>
      <li>
        Try to keep each under 750 words (around ~5 minutes of reading.) Have a
        summary of the project right at the top.
      </li>
      <li>
        Each time you finish a project, do a quick write-up while it is fresh in
        your mind. Before long, you will have a ton of work to share when you
        create a complete portfolio site.
      </li>
    </ol>
