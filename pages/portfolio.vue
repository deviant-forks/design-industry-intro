    <h2>Don't over-invest in your website</h2>
    <p>
      Your portfolio doesn't have to be unique or ornate, as long as it acts as
      an effective vehicle to share your work.
    </p>
    <p>
      The number one mistake designers just coming out of school make is
      spending far too much time creating a portfolio with hardly any work in
      it.
    </p>
    <p>
      Try spending no more than a week on your first portfolio, and check in on
      how you are investing your time by roughly using a 1:4 ratio of time spent
      building your portfolio to working on projects.
    </p>
    <p>
      Your portfolio will be with you for a long time, and you will end up
      changing it frequently. It's ok to invest time in a portfolio website but
      only do so once you are <b>sure</b> you have at least three strong
      projects to show.
    </p>

