      </li>
    </ul>

    <h2>The Basics</h2>
    <ul>
      <li>
        Minimum <b>three</b> projects. Its far better to have a basic site with
        strong projects than a beautiful custom site with a few weak projects.
      </li>
      <li>
        Have <b>one</b> project you can talk about for <b>30 minutes</b>.
        <ul>
          <li>What was the problem?</li>
          <li>How did you arrive at the solution you did?</li>
          <li>What did you do, and who else contributed?</li>
          <li>How much better was the solution?</li>
        </ul>
      </li>
      <li>
        Showcase your organizational skills, collaboration, and a good design
        foundation.
      </li>
      <li>
        Don't put work in your portfolio that you wouldn't want someone to hire
        you to make. If you want to be a web designer, don't put two print
        design projects in your portfolio.
      </li>
    </ul>
    <h2>Getting started</h2>
    <p>
      If you are feeling overwhelmed on where to start, below is a guide to get
      started with the simplest (yet still effective!) portfolio.
    </p>
    <ol>
      <li>
        Create a <a href="https://read.cv/explore">read.cv</a>. This can act as
        both an initial portfolio and an online resume.
      </li>
      <li>
        <a
